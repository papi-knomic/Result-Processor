    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try{
            con=Connect.ConnectDB();
            if (txtMatricNo.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Matric Number","Error", JOptionPane.ERROR_MESSAGE);
                return;

            }
            if (txtStudentName.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter First Name","Error", JOptionPane.ERROR_MESSAGE);
                return;

            }
            if (txtSurname.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Father's name","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (txtAddress.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter address","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (txtContactNo.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter contact no.","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (txtAge.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter age","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (cmbGender.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog( this, "Please select gender","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (cmbDepartment.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog( this, "Please select Department","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (cmbLevel.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog( this, "Please select Level","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Statement stmt;
            stmt= con.createStatement();
            String sql1="Select MatricNo from StudentRegistration where MatricNo= '" + txtMatricNo.getText() + "'";
            rs=stmt.executeQuery(sql1);
            if(rs.next()){
                JOptionPane.showMessageDialog( this, "Matric Number already exists","Error", JOptionPane.ERROR_MESSAGE);
                txtMatricNo.setText("");
                txtMatricNo.requestDefaultFocus();
                return;
            }
            String sql= "insert into StudentRegistration(MatricNo,Fname,Sname,EmailID,ContactNo,Age,Level,Gender,Department,Address)values('"+ txtMatricNo.getText() + "','"+ txtStudentName.getText() + "','"+ txtSurname.getText() + "','"+ txtEmailID.getText() + "','"+ txtContactNo.getText() + "'," + txtAge.getText() + ",'"+ cmbLevel.getSelectedItem() + "','" + cmbGender.getSelectedItem() + "','"+ cmbDepartment.getSelectedItem() + "','" + txtAddress.getText() + "')";

            pst=con.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(this,"Successfully Registered","Student",JOptionPane.INFORMATION_MESSAGE);
            btnSave.setEnabled(false);

        }catch(HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try{
            int P = JOptionPane.showConfirmDialog(null," Are you sure want to delete ?","Confirmation",JOptionPane.YES_NO_OPTION);
            if (P==0)
            {
                con=Connect.ConnectDB();
                String sql= "delete from StudentRegistration where MatricNo = '" + txtMatricNo.getText() + "'";
                pst=con.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(this,"Successfully deleted","Record",JOptionPane.INFORMATION_MESSAGE);

                Reset();
            }
        }catch(HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

