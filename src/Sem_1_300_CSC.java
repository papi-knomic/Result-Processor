    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        // TODO add your handling code here:
        double [] R = new double [14];

        R[0] = Double.parseDouble(txtFRN301.getText());
        R[1] = Double.parseDouble(txtGNS302.getText());
        R[2] = Double.parseDouble(txtCSC301.getText());
        R[3] = Double.parseDouble(txtCSC303.getText());
        R[4] = Double.parseDouble(txtCSC305.getText());
        R[5] = Double.parseDouble(txtCSC307.getText());
        R[6] = Double.parseDouble(txtCSC309.getText());
        R[7] = Double.parseDouble(txtCSC311.getText());
        R[8] = Double.parseDouble(txtCSC313.getText());
        R[9] = Double.parseDouble(txtCSC317.getText());

        R[10] = (((R[0]/20)*2) + ((R[1]/20)*2) + ((R[2]/20)*6) + ((R[3]/20)*3) + ((R[4]/20)*3) + ((R[5]/20)*2) + ((R[6]/20)*3) + ((R[7]/20)*3) + ((R[8]/20)*3) + ((R[9]/20)*2))/29;
        double d = R[10];
        R[11] = R[0] + R[1] + R[2] + R[3] + R[4] + R [5] + R [6] + R [7] + R [8] + R [9];

        String CGPA = String.format("%.3f", d);
        txtGPA.setText(CGPA);
        String TotalScore = String.format("%.00f", R[11]);
        txtTotalScore.setText(TotalScore);

        if (R[10] >= 4.50f)
        {
            txtClass.setText("1st Class");
        }
        else if (R[10] >= 3.70f)
        {
            txtClass.setText("2nd Upper");
        }
        else if (R[10] >= 2.70f)
        {
            txtClass.setText("2nd Lower");
        }
        else if (R[10] >= 2.00f)
        {
            txtClass.setText("3rd Class");
        }
        else if (R[10] >= 0.00f)
        {
            txtClass.setText("Fail");
        }

    }//GEN-LAST:event_btnCalcActionPerformed

