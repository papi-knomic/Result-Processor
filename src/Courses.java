    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try{
            con=Connect.ConnectDB();
            if (txtCourseName.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter course name","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (txtCourseCode.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter course code","Error", JOptionPane.ERROR_MESSAGE);
                return;      
            }
            if (txtLecturer.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Lecturer's name","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (txtCourseUnit.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Please enter Course Unit","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (cmbLevel.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog( this, "Please select gender","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (cmbDepartment.getSelectedItem().equals("")) {
                JOptionPane.showMessageDialog( this, "Please select blood group","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            Statement stmt;
            stmt= con.createStatement();
            String sql1="Select CourseCode from Course where CourseCode= '" + txtCourseCode.getText() + "'";
            rs=stmt.executeQuery(sql1);
            if(rs.next()){
                JOptionPane.showMessageDialog( this, "Course code already exists","Error", JOptionPane.ERROR_MESSAGE);
                txtCourseName.setText("");
                txtCourseName.requestDefaultFocus();
                return;
            }
            String sql= "insert into Course(CourseName,CourseCode,Level,Department,Lecturer,CourseUnit)values('"+ txtCourseName.getText() + "','"+ txtCourseCode.getText()  + "','" + cmbLevel.getSelectedItem() + "','"+ cmbDepartment.getSelectedItem() + "','" + txtLecturer.getText() + "','" + txtCourseUnit.getText() + "')";

            pst=con.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(this,"Successfully saved","Course Information",JOptionPane.INFORMATION_MESSAGE);
            btnSave.setEnabled(false);

        }catch(HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try{
            int P = JOptionPane.showConfirmDialog(null," Are you sure want to delete ?","Confirmation",JOptionPane.YES_NO_OPTION);
            if (P==0)
            {
                con=Connect.ConnectDB();
                String sql= "delete from Course where CourseCode = '" + txtCourseCode.getText() + "'";
                pst=con.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(this,"Successfully deleted","Record",JOptionPane.INFORMATION_MESSAGE);

                Reset();
            }
        }catch(HeadlessException | SQLException ex){
            JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

